package entcascade

import "text/template"

// ---------- template data types ----------

type tmplData struct {
	Package     string
	ImportBlock string
	Nodes       []tmplNode
}

type tmplNode struct {
	Name      string   // "Chatbot"
	IDType    string   // "string"
	CamelName string   // "chatbot" — for error messages
	PkgName   string   // "chatbot" or "billingplan" — predicate package (all lowercase)
	Ops       []tmplOp // single-ID ops
	BatchOps  []tmplOp // batch ops (slice predicates)
}

// tmplOp is a pre-resolved delete operation.
// Exactly one of IsSkip, IsBulk, IsNested, IsSoftDelete, IsUnlink is true.
type tmplOp struct {
	IsSkip       bool
	IsBulk       bool
	IsNested     bool
	IsSoftDelete bool
	IsUnlink     bool
	Comment      string

	// Shared by IsBulk, IsSoftDelete, IsUnlink
	TypeName  string // "Message"
	Predicate string // "message.ChannelID(id)"
	CamelType string // "message"

	// IsSoftDelete only
	SoftDeleteSetter string // "SetDeletedAt"

	// IsUnlink only
	UnlinkClear string // "ClearFolderID"

	// IsNested only
	VarName    string
	QueryType  string
	QueryPred  string
	DeleteType string
	DeletePred string
	CamelQuery string
	ChildOps   []tmplOp
}

